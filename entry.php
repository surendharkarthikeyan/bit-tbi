                <h1 style="margin-bottom: 35px;">INCUBATORS ENTRY AND EXIT POLICY</h1>
            </div>
            <div class="business">
                <h6 class="content-heading">Introduction</h6>
                <div class="content-body">
                    <p>Bannari Amman Institute of Technology (BIT) is committed to create the Centers of Excellence in Engineering, Technology and Management education having relevance to industrial and societal needs. Various policies are framed considering systems, processes and procedures followed in the Institute to ensure the inclusive growth of all the stakeholders associated with the Institute and to "Stay Ahead".</p>
                </div>
                <h6 class="content-heading">Objective</h6>
                <div class="content-body">
                    <p>The main objectives of the incubatee entry/exit policy are to</p>
                    <p>(1) Have a standardized procedure for the induction of new incubatees to TBI.</p>
                    <p>(2) List all the potential sources / channels of entry to TBI as tenants</p>
                    <p>(3) Optimize the idea evaluation carried out before incubatee induction</p>
                    <p>(4) Make it uniform the procedure of incubatee registration to TBI</p>
                    <p>(5) Have rules and regulations in place for incubatee exit from TBI</p>
                    <br>
                </div>
                <h6 class="content-heading">Scope</h6>
                <div class="content-body" >
                    <p>The scope of incubatee entry/exit policy covers all the prospective incubation candidates, pre-incubatees,student, alumni and public incubatees (physical and virtual) including existing firms interested in incubating spin-off ideas.</p>
                </div>
                <h6 class="content-heading">Mandatory References</h6>
                <div class="content-body" >
                    <p>The incubatee entry/exit policy shall confirm to the guidelines of the National Science, Technology and Entrepreneurship Development Board (NSTEDB), DST, New Delhi (in terms of incubation programme, incubation process, time scales, services rendered by TBI etc)</p>
                </div>
                <h6 class="content-heading">Policy Contents</h6>
                <div class="content-body" >
                    <p>Any Individual / group of people (students or general public or mix of the two) can initially propose an idea (to TBI) with prime features as mentioned below</p>
                    <p>(a) Innovativeness</p>
                    <p>(b) Commercial viability</p>
                    <p>(c) Technical feasibility</p>
                    <p>(d) Social sensibility</p>
                    <p>(e) Scalability</p>
                    <p>(f) Capacity to trigger market traction</p>
                    <p>(g) Energy saving / green tech intervention / value addition / low costalternative (in existing product / process based ideas)</p>
                    <p>&#10004; TBI accepts ideas which are from diverse domains of science, technology and engineering. Being in a Tier III town and surrounded by hamlets rich in natural resources, preference will be given to ideas making use of such untapped / less exploited natural resources and for ideas aiming to find solutions that can improve the life and living conditions of the rural folk.</p>
                    <p>&#10004; In a few exceptional cases (if the prospective incubatee desires so) after considering the credentials pertaining to commitment, attitude and track record, the proposed incubatee (on the recommendation of the Idea Screening Committee) may be given access to in-house developed basket-of-technologies (concept proved), to choose one suitable technology based idea and go ahead with business building.</p>
                    <p>&#10004; Ideas screening committee (ISC: TBI Principal Scientist, TBI manager, one technical expert and one business expert) can evaluate the idea and induct the promoter/s as tenant incubatee.</p>
                    <p>&#10004; TBI will organize Idea fest series (Annual Event for BIT Students) and/ or Idea fair (For general public / alumni / students from other campuses) as startup idea screening event(s) either on its own or in collaboration with one or more event partners. Shortlisted finalists from idea fair and /or idea fest series are one of the major sources for incubatees in TBI</p>
                    <p>&#10004; Students or general public, who could register an idea as a pre-incubation lead to TBI, can opt for proof-of-concept and beyond. They (if such idea leads are proven as positive) can register as incubatee firm/s with the approval of ISC.</p>
                    <p>&#10004; Potential future startup promoter/s recommended through BIT- Business club or campus E-Cell or innovation lab can register their idea / s for incubation via ISC of TBI</p>
                    <p>&#10004; Registration is provided to all the tenant incubatees /firms upon submitting the following</p>
                    <p>• PAN/Aadhar card</p>
                    <p>• Executive summary of the idea</p>
                    <p>• Permanent address proof</p>
                    <p>• Two references</p>
                    <p>• Company incorporation certificate, if any</p>
                    <p>• Filled-in application with passport size photograph of promoters</p>
                    <p>• Signed agreement (between incubatee & incubator) indicating incubatee agreeing to abide by the rules and regulations to be adhered to by incubatees while they exist as tenant firms / virtual incubatees</p>
                    <p>&#10004; The regular incubatee registration fee is Rs.5000 (One-time payment) for availing TBI services (Networking, Mentoring, Capacity building, Tech development initiatives, Imparting business, Facilitation, advisory/ legal aspects etc.) Physical incubatee firms intending to avail office cubicles will have to remit Rs.1500 for every 3 months as rental fee.</p>
                    <p>&#10004; Incubatees will be charged for specialized services such as equipment usage charges while technology development / validation or business support service charges (such charges are pre-fixed, nominal and mostly are on hourly basis)</p>
                    <p>&#10004; Registered tenant companies will be permitted to exit from TBI when they fulfill one or more than one of the following criteria</p>
                    <p>(a) When the incubatee /tenant firm reaching twenty four months for IT, EEE, ICT, Electronics and Instrumentation, Food technology, Social entrepreneurs etc., based incubated companies. Three years for biotechnology, agriculture and manufacturing based incubated companies.</p>
                    <p>(b) Under performance / not reaching business milestones /unfavorable existence etc., would force TBI management to review continuance of the tenant on case-to-case basis.</p>
                    <p>(c) When the sales annual turnover of the company exceeds 50 Lakhs or when the company profit before interest and tax exceeds 25 Lakhs annually.</p>
                    <p>(d) Raising handsome investments from secondary funding sources (Angel, VC etc.) Rs. 50 Lakhs or more.</p>
                    <p>(e) When the number of employees of the registered firm exceeds 15 members.</p>
                </div>
                <h6 class="content-heading">Advisory Board and Review Mechanism</h6>
                <div class="content-body" >
                    <p>Board of Governors of BIT-TBI (BoG, appointed by the Annual General Meeting) (comprise of one academician, two industrialists, two industry association members, one bank / angel person, two government representatives and one R&D person) shall review the implementation of policy at least once in a year to ensure the compliance of policy and implementation of guidelines stated in the policy. Committee is also empowered to make suggestions for revision of the policy to the Committee responsible for framing this policy.</p>
                </div>
                <h6 class="content-heading">Approval Procedure of Policy</h6>
                <div class="content-body" >
                    <p>Incubatee entry / exit policy of BIT-TBI shall be approved by the AGM of BIT-TBI; any revisions that arise subsequently shall be reviewed as said above and forwarded to AGM through Member Secretary of the Council for amendments of the policy. On approval of the amendments, revised policy shall be published and circulated to all members appropriately.</p>
